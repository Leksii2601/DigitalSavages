// ================================================
// TRANSLATION FUNCTION
// ================================================

function translatePage(lang) {
  const elements = document.querySelectorAll('[data-translate]');
  
  elements.forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang] && translations[lang][key]) {
      element.innerHTML = translations[lang][key];
    }
  });
  
  // Зберігаємо вибрану мову
  localStorage.setItem('selectedLanguage', lang);
}

// ================================================
// INITIALIZE TRANSLATION ON PAGE LOAD
// ================================================

document.addEventListener('DOMContentLoaded', function() {
  // Отримуємо збережену мову або встановлюємо українську за замовчуванням
  const savedLanguage = localStorage.getItem('selectedLanguage') || 'uk';
  
  // Чекаємо поки завантажаться компоненти
  setTimeout(() => {
    translatePage(savedLanguage);
    
    // Оновлюємо відображення кнопки мови
    const currentLangElement = document.querySelector('.current-lang');
    if (currentLangElement) {
      currentLangElement.textContent = savedLanguage === 'en' ? 'EN' : 'UA';
    }
  }, 100);
});
